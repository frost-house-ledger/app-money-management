export function getCategoryName(id, locale = 'jp') {
  if (!id) return '';
  const entry = TRANSLATIONS.categoryNames && TRANSLATIONS.categoryNames[id];
  if (!entry) return id;
  const code = String(locale || '').toLowerCase();
  const base = code.split('-')[0];

  // normalize common locale codes to our translation keys
  const map = {
    jp: 'jp',
    ja: 'jp',
    de: 'de',
    en: 'en',
    es: 'es',
    pt: 'pt',
    it: 'it',
    fr: 'fr',
    ru: 'ru',
    tw: 'tw',
    zh: 'tw',
    ko: 'ko',
    ar: 'ar'
  };

  const target = map[code] || map[base] || null;
  const order = [target, 'en', 'jp'];

  for (const k of order) {
    if (!k) continue;
    if (entry[k]) return entry[k];
  }

  // fallback picks any available
  return entry.en || entry.jp || Object.values(entry)[0] || id;
}

export function getMessages(locale) {
  const raw = (locale || '').toLowerCase();

  // map common locale codes to our translation keys
  const map = {
    jp: 'jp',
    ja: 'jp',
    de: 'de',
    en: 'en',
    es: 'es',
    pt: 'pt',
    it: 'it',
    fr: 'fr',
    ru: 'ru',
    tw: 'tw',
    ko: 'ko',
    ar: 'ar'
  };

  const base = raw.split('-')[0];
  let target = map[raw] || map[base] || null;

  // fallback logic: prefer requested locale key, then English, then Japanese
  const fallbackOrder = [target, 'en', 'jp'];

  return Object.fromEntries(
    Object.entries(TRANSLATIONS).map(([key, value]) => {
      const chosen = fallbackOrder.map(t => (t ? value[t] : undefined)).find(v => v !== undefined && v !== null);
      return [key, chosen || ''];
    })
  );
}

export function formatMessage(template, values = {}) {
  return Object.entries(values).reduce((text, [key, value]) => {
    return text.replace(`{${key}}`, String(value));
  }, template);
}
